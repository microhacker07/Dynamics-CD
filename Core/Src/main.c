	HAL_GPIO_WritePin(SPI1_BARO_NCS_GPIO_Port, SPI1_BARO_NCS_Pin, GPIO_PIN_SET);
	HAL_GPIO_WritePin(SPI1_FLASH_NCS_GPIO_Port, SPI1_FLASH_NCS_Pin, GPIO_PIN_SET);

	// LED set high
